      const drawer = document.getElementById("drawer");
      const overlay = document.getElementById("overlay");
      const burger = document.getElementById("burger");
      const cover = document.getElementById("cover");
      const pages = document.getElementById("pages");
      const drawerLinks = document.getElementById("drawerLinks");

      function isMobile() {
        return window.matchMedia("(max-width: 899px)").matches;
      }

      function toggleDrawer(force) {
        // desktop: drawer always open, no overlay
        if (!isMobile()) {
          drawer.classList.remove("open");
          overlay.classList.remove("show");
          document.body.classList.remove("drawer-open");
          burger && burger.setAttribute("aria-expanded", "false");
          burger && (burger.innerHTML = "☰");
          return;
        }

        const open =
          typeof force === "boolean"
            ? force
            : !drawer.classList.contains("open");
        drawer.classList.toggle("open", open);
        overlay.classList.toggle("show", open);
        document.body.classList.toggle("drawer-open", open);

        burger.setAttribute("aria-expanded", String(open));
        burger.innerHTML = open ? "✕" : "☰";
        burger.setAttribute(
          "aria-label",
          open ? "Close navigation menu" : "Open navigation menu"
        );
      }

      function showLesson(id) {
        cover.style.display = "none";
        document
          .querySelectorAll(".page")
          .forEach((p) => p.classList.remove("active"));
        const el = document.getElementById(id);
        if (el) {
          el.classList.add("active");
          history.replaceState(null, "", "#" + id);
          document
            .getElementById("mainContent")
            .scrollIntoView({ behavior: "smooth" });
        }
        if (isMobile()) toggleDrawer(false);
      }

      function showCover() {
        document
          .querySelectorAll(".page")
          .forEach((p) => p.classList.remove("active"));
        cover.style.display = "block";
        history.replaceState(null, "", "#");
        if (isMobile()) toggleDrawer(false);
      }

      document.addEventListener("keydown", (e) => {
        if (
          e.key === "Escape" &&
          isMobile() &&
          drawer.classList.contains("open")
        ) {
          toggleDrawer(false);
        }
      });

      window.addEventListener("resize", () => {
        // When switching to desktop, ensure no mobile overlay/lock remains
        if (!isMobile()) {
          overlay.classList.remove("show");
          document.body.classList.remove("drawer-open");
          drawer.classList.remove("open");
          burger && (burger.innerHTML = "☰");
          burger && burger.setAttribute("aria-expanded", "false");
        }
      });

      function renderBook() {
        pagesData.forEach((page) => {
          // Page card
          const pageDiv = document.createElement("section");
          pageDiv.className = "page";
          pageDiv.id = page.id;

          const h2 = document.createElement("h2");
          h2.textContent = page.title;
          pageDiv.appendChild(h2);

          const wrap = document.createElement("div");
          wrap.className = "table-wrap";

          const table = document.createElement("table");
          const thead = document.createElement("thead");
          const tbody = document.createElement("tbody");

          thead.innerHTML =
            "<tr><th>English Word</th><th>Transcription (IPA)</th><th>Tajik Translation</th></tr>";
          table.appendChild(thead);

          page.rows.forEach((r) => {
            const tr = document.createElement("tr");
            tr.innerHTML = `
            <td>${r[0]}</td>
            <td class="ipa">${r[1]}</td>
            <td>${r[2]}</td>
          `;
            tbody.appendChild(tr);
          });

          table.appendChild(tbody);
          wrap.appendChild(table);
          pageDiv.appendChild(wrap);
          pages.appendChild(pageDiv);

          // Drawer link (insert before About)
          const a = document.createElement("a");
          a.href = "#" + page.id;
          a.textContent = page.title;
          a.addEventListener("click", (e) => {
            e.preventDefault();
            showLesson(page.id);
          });

          const aboutLink = drawerLinks.querySelector(".about-link");
          drawerLinks.insertBefore(a, aboutLink);
        });

        // open hash lesson if exists
        const hash = location.hash.replace("#", "");
        if (hash) {
          showLesson(hash);
        }
      }

